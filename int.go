// Write a Go program to declare and print different interger types(int, int8, int16, int 32, int64 & uint)

package main

import "fmt"

var (
	z    int    = 34
	a    int8   = 35
	b    int16  = 36
	c    int32  = 37
	d    int64  = 38
	e    uint   = 39
	name string = "Block"
)

func main() {
	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(z)
	fmt.Println(name)
}

